## UPPER FUNCTION

a = "Hello, World!"
print(a.upper())
#----------------

## Lower Case function 

a = "Hello, World!"
print(a.lower())
#-------------------

## strip function

a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"
#--------------------------

## replace function

a = "Hello, World!"
print(a.replace("H", "J"))
#--------------------------------

## Split String function

a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']